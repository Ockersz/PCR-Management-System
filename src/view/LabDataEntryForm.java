    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:

        try {

            ManageTestDetails test = new ManageTestDetails();
            ResultSet result = test.loadTestbyID(txtSearch.getText());
            ((DefaultTableModel) tblTestInfo.getModel()).setNumRows(0);
            DefaultTableModel model = (DefaultTableModel) tblTestInfo.getModel();
            //System.out.println(result.getString("vname"));

            while (result.next()) {
                Vector vec = new Vector();
                vec.add(result.getString("sampleid"));//1
                vec.add(result.getString("date"));//2
                vec.add(result.getString("status"));//3
                vec.add(result.getString("vid"));//4
                vec.add(result.getString("vtel"));//5
                vec.add(result.getString("vname"));//6
                vec.add(result.getString("vgender"));//7
                vec.add(result.getInt("vage"));//7
                vec.add(result.getString("vaddres"));//8
                vec.add(result.getString("Province"));//9
                vec.add(result.getString("District"));//10
                model.addRow(vec);

            }

            refreashTable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(MessageManagement.getVisibleContent(), "Data Transit Failed \n\r" + e.getClass() + "\n\r" + e.getMessage(), "Test Details Management", JOptionPane.INFORMATION_MESSAGE);

        } catch (ArrayIndexOutOfBoundsException e) {

        } catch (Exception e) {
            JOptionPane.showMessageDialog(MessageManagement.getVisibleContent(), "Data Transit Failed \n\r" + e.getClass() + "\n\r" + e.getMessage(), "Test Details Management", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_txtSearchKeyReleased

