    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        ManageTestDetails test = new ManageTestDetails();
        boolean i;
        if (txtSampleId.getText().length() == 0) {
            JOptionPane.showMessageDialog(MessageManagement.getVisibleContent(), "Enter the Sample Id(Test Id) \nYou want to Update", "Data Management ", JOptionPane.INFORMATION_MESSAGE);
        } else {
            i = test.checkSampleId(txtSampleId.getText());
            if (i) {

                i = test.updateResults(txtSampleId.getText(), (String) cmbResult.getSelectedItem());
                if (i) {
                    JOptionPane.showMessageDialog(MessageManagement.getVisibleContent(), "Results Updated Successfully ", "Data Management ", JOptionPane.INFORMATION_MESSAGE);
                    refreashTable();
                } else {
                    JOptionPane.showMessageDialog(MessageManagement.getVisibleContent(), " Results Update Failed ", "Data Management ", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(MessageManagement.getVisibleContent(), "Invalid Test Id\nMake sure you are entering results to an Exhisting record.\nIf not please use 'Insert' option ", "Data Management ", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

